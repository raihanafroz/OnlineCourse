    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getRowCount()>0){
        Connection con=OnlineCourse.Conn();
        PreparedStatement ps;
        try{
            ps = con.prepareStatement("Insert into COURSEOFFER (COURSENAME,COURSECODE,COURSETEACHER,BATCH,SEMESTER,LASTDATE) values (?,?,?,?,?,?)");
            for(int row = 0; row<jTable1.getRowCount(); row++)
            {
                String coursename = (String)jTable1.getValueAt(row, 0);
                String courseCode = (String)jTable1.getValueAt(row, 1);
                String courseTeacher = (String)jTable1.getValueAt(row, 3);
                String semester = (String)jTable1.getValueAt(row, 4);
                ps.setString(1, coursename);
                ps.setString(2, courseCode);
                ps.setString(3, courseTeacher);
                ps.setString(4, batch);
                ps.setString(5, semester);
                ps.setString(6, currentDate);
                
                ps.addBatch();
            }
            ps.executeBatch();
            JOptionPane.showMessageDialog(rootPane, "Successfully offered");
            dispose();
        }catch(Exception e){
            
        }
        }else{
            JOptionPane.showMessageDialog(rootPane, "No data fond into table");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

