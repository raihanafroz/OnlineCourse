    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedItem().toString().equals("    --Select--")||jComboBox2.getSelectedItem().toString().equals("        ----Select----")||jComboBox3.getSelectedItem().toString().equals("        ----Select----")){
            JOptionPane.showMessageDialog(null, "You have to select all selection box");
        }else{
            Object [] row={jComboBox2.getSelectedItem().toString(),courseOfferClass.getCourseCode(jComboBox2.getSelectedItem().toString()),courseOfferClass.getCoursePREREQUISITE(courseOfferClass.getCourseCode(jComboBox2.getSelectedItem().toString())),jComboBox3.getSelectedItem().toString(),jComboBox1.getSelectedItem().toString().concat("("+jYearChooser1.getValue()+")")};
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String samenot= "";
            boolean exists = false;
            for (int i=0; i<jTable1.getRowCount(); i++){    
                samenot= jTable1.getValueAt(i, 1).toString();
                if(courseOfferClass.getCourseCode(jComboBox2.getSelectedItem().toString()).equals(samenot)){
                    exists = true;
                    break;
                }
            }
            boolean existsSemester = true;
            String same="";
            for (int i=0; i<jTable1.getRowCount(); i++){    
                same= jTable1.getValueAt(i, 4).toString();
                if(jComboBox1.getSelectedItem().toString().concat("("+jYearChooser1.getValue()+")").equals(same)){
                }else{
                    existsSemester=false;
                    break;
                }
            }
            if(existsSemester){ 
                if(!exists){ 
                    model.addRow(row);
                }else {
                    JOptionPane.showMessageDialog(null, "This course already added for offer.","Duplicate",JOptionPane.ERROR_MESSAGE);
                }
            }else {
                JOptionPane.showMessageDialog(null, "You cann't offer different semester at a time.","Message",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

