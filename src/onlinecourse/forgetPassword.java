    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt){
        if(jPasswordField1.getText().equals(jPasswordField2.getText())){
            if(mm=="Advisor"){
                if(loginUser.updateAdvisor(Integer.parseInt(jTextField1.getText()), jPasswordField1.getText(), jTextField2.getText())){
                    JOptionPane.showMessageDialog(rootPane, "Successfully Changed.");
                    forgetPasswordframeCount=0;
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Something going to wrong.");
                }
            }else if(mm=="Teacher"){
                if(loginUser.updateTeacher(Integer.parseInt(jTextField1.getText()), jPasswordField1.getText(), jTextField2.getText())){
                    JOptionPane.showMessageDialog(rootPane, "Successfully Changed.");
                    forgetPasswordframeCount=0;
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Something going to wrong.");
                }
            }else if(mm=="Student"){
                if(loginUser.updateStudent(Integer.parseInt(jTextField1.getText()), jPasswordField1.getText(), jTextField2.getText())){
                    JOptionPane.showMessageDialog(rootPane, "Successfully Changed.");
                    forgetPasswordframeCount=0;
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Something going to wrong.");
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Type missing.");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Password not match.");
        }
    }
    
