        // TODO add your handling code here:
        if(jRadioButton1.isSelected()){
            mm=jRadioButton1.getText();
        }else if(jRadioButton2.isSelected()){
            mm=jRadioButton2.getText();
        }else if(jRadioButton3.isSelected()){
            mm=jRadioButton3.getText();
        }
        if(mm!="" &&! jTextField1.getText().isEmpty() &&! jTextField2.getText().isEmpty()){
            if(mm=="Advisor"){
                if(loginUser.checkAdvisor(Integer.parseInt(jTextField1.getText()), jTextField2.getText())){
                    jPanel2.setVisible(false);
                    abcd();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Wrong ID or SecurityKey.");
                }
            }else if(mm=="Teacher"){
                if(loginUser.checkTeacher(Integer.parseInt(jTextField1.getText()), jTextField2.getText())){
                    jPanel2.setVisible(false);
                    abcd();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Wrong ID or SecurityKey.");
                }
            }else if(mm=="Student"){
                if(loginUser.checkStudent(Integer.parseInt(jTextField1.getText()), jTextField2.getText())){
                    jPanel2.setVisible(false);
                    abcd();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Wrong ID or SecurityKey.");
                }
            }else{
                
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "not ready");
        }
